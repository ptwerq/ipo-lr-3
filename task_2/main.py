# Запрашиваем у пользователя два числа
num1 = int(input("Введите первое число: "))  # Получаем ввод первого числа и превращаем его в целое число
num2 = int(input("Введите второе число: "))  # Получаем ввод второго числа и превращаем его в целое число

# Используем тернарное выражение для выбора наименьшего числа
result = num1 if num1 < num2 else (num2 if num2 < num1 else None)  
# Если num1 меньше num2, result = num1
# Иначе, если num2 меньше num1, result = num2
# Если числа равны, result = None

# Вывод результата
if result is None:  # Проверяем, равны ли числа
    print("Числа равны!")  # Если равны, выводим сообщение
else:
    print(f"Наименьшее число: {result}")  # Иначе выводим наименьшее число

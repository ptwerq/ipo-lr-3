# Запрашиваем у пользователя целое число
number = int(input("Введите целое число: "))

# Проверяем, делится ли число на 2 без остатка
if number % 2 == 0:
    # Если остаток равен 0, значит число чётное
    print(f"Число {number} является чётным.")
else:
    # Если остаток не равен 0, значит число нечётное
    print(f"Число {number} является нечётным.")
import math  # импортируем библиотеку math, чтобы использовать функции вроде sqrt() для извлечения квадратного корня

# Запрашиваем у пользователя коэффициенты квадратного уравнения
a = float(input("Введите коэффициент a: "))  # коэффициент при x²
b = float(input("Введите коэффициент b: "))  # коэффициент при x
c = float(input("Введите коэффициент c: "))  # свободный член

# Вычисляем дискриминант по формуле D = b² - 4ac
D = b**2 - 4*a*c

# Выводим значение дискриминанта
print(f"Дискриминант: {D}")

# Проверяем, сколько действительных корней имеет уравнение
if D > 0:  # если дискриминант положительный, то есть два разных действительных корня
    x1 = (-b + math.sqrt(D)) / (2*a)  # первый корень
    x2 = (-b - math.sqrt(D)) / (2*a)  # второй корень
    print(f"Уравнение имеет два действительных корня: x1 = {x1}, x2 = {x2}")
elif D == 0:  # если дискриминант равен нулю, то корень один (x1 = x2)
    x = -b / (2*a)  # вычисляем корень
    print(f"Уравнение имеет один действительный корень: x = {x}")
else:  # если дискриминант отрицательный, действительных корней нет
    print("Уравнение не имеет действительных корней.")

# Запрашиваем у пользователя день (целое число)
day = int(input("Введите день: "))

# Запрашиваем у пользователя месяц (целое число)
month = int(input("Введите месяц (числом): "))

# Проверяем, к какому времени года относится введённый месяц
if 3 <= month <= 5:     # Если месяц — март, апрель или май
    season = "Весна"
elif 6 <= month <= 8:   # Если месяц — июнь, июль или август
    season = "Лето"
elif 9 <= month <= 11:  # Если месяц — сентябрь, октябрь или ноябрь
    season = "Осень"
elif month == 12 or month <= 2:  # Если месяц — декабрь, январь или февраль
    season = "Зима"
else:                   # Если введён неверный номер месяца
    season = "Некорректный месяц"

# Выводим результат — сезон, который соответствует введённому месяцу
print(f"Сезон: {season}")

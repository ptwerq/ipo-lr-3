import math  # импортируем библиотеку math, чтобы использовать sqrt() для вычисления квадратного корня

# Вводим координаты точки и радиус круга
x = float(input("Введите координату x: "))  # координата точки по оси X
y = float(input("Введите координату y: "))  # координата точки по оси Y
R = float(input("Введите радиус круга R: "))  # радиус круга

# Вычисляем расстояние от точки (x, y) до центра (0, 0) по формуле distance = √(x² + y²)
distance = math.sqrt(x**2 + y**2)

# Сравниваем расстояние от центра до точки с радиусом круга
if distance <= R:  # если расстояние меньше или равно радиусу
    print("Точка находится внутри круга или на его границе.")  # значит точка внутри или на окружности
else:  # иначе
    print("Точка находится вне круга.")  # точка находится снаружи круга
